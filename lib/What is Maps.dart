//                                      What is Map in Dart Programming ?


// In Dart Programming, Maps are Dictionary-like data types that exist in key-value form(know as lock-key)

// There is no restriction on the type of data that goes in a map data type.

// Maps are very flexible and can mutate their size based on the requirements.

// It is important to note that all locks(keys) need to be unique a map data type.

